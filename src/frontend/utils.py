def load_translations(lang: str) -> Dict[str, Any]:
    """Loads translation dictionary for a given language."""
    translation_base_path = relative_project_path("public/locales")
    translation_file_path = translation_base_path / lang / "translations.json"

    try:
        with open(translation_file_path, "r", encoding="utf-8") as f:
            return json.load(f)
    except FileNotFoundError:
        print(
            f"Warning: Translation file not found for language '{lang}' at '{translation_file_path}'. Falling back to 'en'."
        )
        # Fallback to English if the requested language file isn't found
        english_translation_path = translation_base_path / "en" / "translations.json"

        try:
            with open(english_translation_path, "r", encoding="utf-8") as f:
                return json.load(f)
        except FileNotFoundError:
            print(
                f"Error: English translation file also not found at '{english_translation_path}'. Please check your public/locales structure."
            )
            return {}  # Return empty dict if fallback also fails
    except json.JSONDecodeError:
        print(f"Error: Could not decode JSON from translation file '{translation_file_path}'.")
        return {}
    except Exception as e:
        print(f"An unexpected error occurred while loading translations: {e}")
        return {}


